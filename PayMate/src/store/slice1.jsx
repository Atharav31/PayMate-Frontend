import { createSlice } from "@reduxjs/toolkit";

const slice1 = createSlice({
  name: "user",
  initialState: {
    user: null,
  },
  reducers: {
    login: (state, action) => {
      state.user = action.payload;
    },
    logout: (state) => {
      state.user = null;
    },
  },
});

export const { login, logout } = slice1.actions;
export default slice1.reducer;
